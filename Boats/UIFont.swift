import UIKit

extension UIFont {
    static var small: UIFont {
        return .systemFont(ofSize: 11.0)
    }
    
    static var medium: UIFont {
        return .systemFont(ofSize: 13.0)
    }
    
    static var large: UIFont {
        return .boldSystemFont(ofSize: 17.0)
    }
}
